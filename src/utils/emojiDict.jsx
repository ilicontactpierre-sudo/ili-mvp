// в”Җв”Җ Fonction d'application в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
export function applyEmojiMode(text) {
  if (!text) return text

  // DГ©coupe en tokens : mots + ponctuation + espaces
  const tokens = text.split(/(\s+|[.,!?;:В«В»"'()[\]{}\-вҖ“вҖ”вҖҰ])/)

  return tokens.map((token, i) => {
    // Espaces et ponctuations : passer tels quels
    if (/^\s*$/.test(token) || /^[.,!?;:В«В»"'()[\]{}\-вҖ“вҖ”вҖҰ]+$/.test(token)) {
      return token
    }

    // Nettoyer le token : minuscules, sans ponctuation collГ©e
    const clean = token
      .toLowerCase()
      .replace(/^[^a-zГЎГ ГўГӨГ©ГЁГӘГ«ГӯГ¬Г®ГҜГіГІГҙГ¶ГәГ№Г»ГјГҪГҝГҰЕ“Г§]+/, '')
      .replace(/[^a-zГЎГ ГўГӨГ©ГЁГӘГ«ГӯГ¬Г®ГҜГіГІГҙГ¶ГәГ№Г»ГјГҪГҝГҰЕ“Г§]+$/, '')

    const emoji = EMOJI_DICT[clean]
    if (emoji) {
      return (
        <span key={i} title={token} style={{ cursor: 'default' }}>
          {emoji}
        </span>
      )
    }

    return token
  })
}

